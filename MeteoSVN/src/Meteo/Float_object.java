package Meteo;


public class Float_object {

	public Float_object(float value) {
		super();
		this.value = value;
	}

	/**
	 * @uml.property  name="value"
	 */
	private float value;

	/**
	 * Getter of the property <tt>value</tt>
	 * @return  Returns the value.
	 * @uml.property  name="value"
	 */
	public float getValue() {
		return value;
	}
}
